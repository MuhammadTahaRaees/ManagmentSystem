    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = JOptionPane.showInputDialog(this, "Id:");
        if (id != null) {
            try {

                String query = "select * from reception where id = '" + id + "'";
                ResultSet rs = Db.getData(query);
                if (rs.next()) {
                    query = "DELETE FROM reception WHERE id=" + id + "";
                    Db.setData(query);
                    JOptionPane.showMessageDialog(null, "Record deleted...");
                    display();
                } else {
                    JOptionPane.showMessageDialog(this, "ID does not exist..");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String server = "reception";
        String res = JOptionPane.showInputDialog(this, "Enter ID..");
        if (res != null) {
            try {
                String query = "select id from reception where id = '" + res + "'";
                ResultSet rs = Db.getData(query);
                if (rs.next()) {
                    this.dispose();
                    new UpdRecep(name, server, id, Integer.parseInt(res)).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Id does not exists..");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }

        }

    }//GEN-LAST:event_jButton4ActionPerformed

